    private boolean validarDatos() {
        
        if (jTextFieldNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El campo nombre esta vacío");
            return false;
        }
        if(jTextFieldApellidos.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "El campo apellidos esta vacío");
            return false; 
        }
        if (jTextFieldNaci.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El campo fecha de nacimiento esta vacío");
            return false;
        }
        if (jTextFieldAlta.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El campo fecha de alta esta vacío");
            return false;
        }
        if(!Convert.isValidDate(jTextFieldNaci.getText())){
            JOptionPane.showMessageDialog(null, "El campo fecha de nacimiento no es valido");
            return false; 
        }
        if(!Convert.isValidDate(jTextFieldAlta.getText())){
            JOptionPane.showMessageDialog(null, "El campo fecha de alta no es valido");
            return false; 
        }
        
        return true;
    }
    
    private void pantallaCrear() {
        jLabelTitulo.setText("Nuevo empleado");
        jButtonAceptar.setText("Crear");
        llenarComboBox();
    }
    
    private void pantallaEditar() {
        Empleado empleado;
        
        jLabelTitulo.setText("Editar empleado");
        jButtonAceptar.setText("Editar");
        
        empleado = empleadoDAO.findById(id);
        
        jTextFieldNombre.setText(empleado.getNombre());
        jTextFieldApellidos.setText(empleado.getApellidos());
        jTextFieldNaci.setText(empleado.getNaci().toString());
        jTextFieldAlta.setText(empleado.getAlta().toString());
        
        llenarComboBox();
        
        jComboBoxCargo.setSelectedItem(empleado.getCargo());
        jComboBoxIdOficina.setSelectedItem(empleado.getIdOficina());
    }
    
    private void llenarComboBox() {

        List<String> listaCargo = empleadoDAO.cargo();

        for (int i = 0; i < listaCargo.size(); i++) {
            jComboBoxCargo.addItem(listaCargo.get(i));
        }

        List<Integer> listaOfi = empleadoDAO.idOficina();

        for (int i = 0; i < listaOfi.size(); i++) {
            jComboBoxIdOficina.addItem(listaOfi.get(i));
        }

    }

