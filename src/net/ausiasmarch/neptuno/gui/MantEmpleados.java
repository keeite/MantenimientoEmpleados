    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        if (!validarDatos()) {
            return;  
        }
        String nombre    = jTextFieldNombre.getText();
        String apellidos = jTextFieldApellidos.getText();
        int cargo        = Convert.parseInt(jComboBoxCargo.getSelectedItem().toString());
        Long oficina     = Convert.parseLong(jComboBoxIdOficina.getSelectedItem().toString());
        Date fechaNaci   = Convert.parseDate(jTextFieldNaci.getText());
        Date fechaAlta   = Convert.parseDate(jTextFieldAlta.getText());
        String ciudad = oficinaDAO.findById(oficina).getCiudad();
        int oficinaint = Convert.parseInt(jComboBoxIdOficina.getSelectedItem().toString());
        
        java.sql.Date sqlNaci = new java.sql.Date(fechaNaci.getTime());
        java.sql.Date sqlAlta = new java.sql.Date(fechaAlta.getTime());
        
        Empleado e = new Empleado(id, nombre, apellidos, cargo, oficinaint, sqlNaci, sqlAlta);
        
        empleadoDAO.create(e);
    }//GEN-LAST:event_jButtonAceptarActionPerformed

